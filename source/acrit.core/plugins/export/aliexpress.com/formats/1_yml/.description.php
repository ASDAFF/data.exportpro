<h2>Характеристики</h2>
<p><b>Внимание!</b> Для каждой категории в базе данных AliExpress определены обязательные и необязательные атрибуты.</p>
<p>При загрузке файла, Вам будет предложено сопоставить наименования атрибутов/значений в вашем файле с обязательными атрибутами в базе данных AliExpress.</p>
<p>Некоторые атрибуты общие для карточки товара, некоторые определяют SKU (цвет, размер, объем памяти). Система распределит их автоматически</p>
<p>Количество указанных атрибутов напрямую влияет на решение о покупке, и поисковую оптимизацию, поэтому наша рекомендация - вносить в файл как можно больше атрибутов.</p>
<p><br/></p>

<h2>Как определить набор характеристик для каждого раздела?</h2>
<p>Определить категории можно только из шаблонных Excel-файлов. Для этого перейдите в «Управление товаром», далее нажмите кнопку «Загрузка Excel» (вверху справа), нажмите ссылку «Скачать шаблон», выберите нужную категорию, ниже выберите русский язык и нажмите кнопку «Скачать шаблон». В скачанном файле есть группа столбцов «Свойства информации» (с желтым фоном), это и есть свойства.</p>
